#  Aqui são mostradas as operações básicas do Python (calculadora)
# A função print imprime na tela uma representação em string
# do argumento passado para ela

# Adição
print(5 + 2)  # 7

# Subtração
print(2 - 5)  # -3

# Multiplicação
print(7 * 7)  # 49

# Divisão
print(6 / 2)  # 3.0 - retorna float, mesmo se resultado for exato

# Exponenciação
print(2 ** 10)  # 1024

# Divisão inteira:
# d = (Q * D) + R
# d - dividendo
# D - divisor
# Q = quociente
# R = resto

# Obtendo o quociente inteiro
print(8 // 5)  # 1

# Obtendo o resto
print(8 % 5)  # 3

